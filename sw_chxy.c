#include "fillit.h"

int		sw_chxy(t_point **e, int i)
{
	if ((*e)->y == i && (*e)->x == i)
		return (1);
	else if ((*e)->x == i)
	{
		(*e)->x = 0;
		(*e)->y = -~(*e)->y;
	}
	else
		(*e)->x = -~(*e)->x;
	return (0);
}

void	sw_exit(void)
{
	write(1, "error\n", 6);
	exit(0);
}
